const a = 5
let b = 3

b+=a //Atribuição aditiva, Estamos Acrescentando o valoar de A ao de B
console.log(b)

b -= 4 // Atribuição Subtrativa , B = B - 4
console.log(b)

b *= 2 // Atribuição Multiplicativa, B = B * 2
console.log(b)

b /= 2 // Atribuição Divisiva, B = B / 2
console.log(b)

b %= 2 // Atribuição Modular, B = B % 2 
console.log(b)